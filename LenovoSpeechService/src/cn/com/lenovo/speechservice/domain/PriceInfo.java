package cn.com.lenovo.speechservice.domain;

import java.util.ArrayList;

/**
 * 保存返回的价格类型的数据
 * @author kongqw
 *
 */
public class PriceInfo {
	// 状态码
	public int code;
	// 文字内容
	public String text;
	// 价格信息
	public ArrayList<Price> list;

	/**
	 * 价格信息
	 * @author kongqw
	 *
	 */
	public class Price {
		// 名称
		public String name;
		// 价格
		public String price;
		// 详情地址
		public String detailurl;
		// 图标地址
		public String icon;
	}
}
