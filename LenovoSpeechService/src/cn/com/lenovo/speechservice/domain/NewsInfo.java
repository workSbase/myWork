package cn.com.lenovo.speechservice.domain;

import java.util.ArrayList;

/**
 * 保存返回的新闻类型的数据
 * @author kongqw
 *
 */
public class NewsInfo {
	// 状态码
	public int code;
	// 文字内容
	public String text;
	// 新闻
	public ArrayList<News> list;

	/**
	 * 新闻实体
	 * @author kongqw
	 *
	 */
	public class News {
		// 新闻标题
		public String article;
		// 新闻来源
		public String source;
		// 新闻详细地址
		public String detailurl;
		// 新闻图标地址
		public String icon;
	}
}
